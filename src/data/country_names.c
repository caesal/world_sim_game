const char *country_name_localized(int name_id, int language) {
    if (name_id < 0 || name_id >= COUNTRY_NAME_COUNT) return "";
    return language == 1 ? COUNTRY_NAME_RULES[name_id].zh : COUNTRY_NAME_RULES[name_id].en;
}

int country_name_find_by_text(const char *name) {
    int i;

    if (!name || !name[0]) return -1;
    for (i = 0; i < COUNTRY_NAME_COUNT; i++) {
        if (strcmp(name, COUNTRY_NAME_RULES[i].en) == 0 ||
            strcmp(name, COUNTRY_NAME_RULES[i].zh) == 0) {
            return i;
        }
    }
    return -1;
}
